class Banana {
  private:
    int _x = 500;
    uint8_t _y = 0;
    uint8_t timer = 0;
    uint8_t frame = 0;
    float _acceleration = 0.04;
    float _velocity = 0;


  public:

    int getX() {
      return _x;
    }

    int getY() {
      return _y;
    }

    void spawn() {
      _x = 160 + random(60);
      _y = 6 + random(36);
      _acceleration = 0.04;
      _velocity = 0;
    }

    void update() {
      // Determine location
      _velocity += _acceleration;
      _x -= _velocity;

      if (_y <= 0) {
        _y = 6 + random(36);
      }

      if (_x < -60) {
        spawn();   
      }

      // Animate banana and reset timer
      frame = timer / 8;

      timer++;

      if (timer >= 64)
      {
        timer = 0; 
      }
    }

    void draw() {
      Sprites::drawPlusMask(_x, _y, bananaSpr, frame);
    }
};

